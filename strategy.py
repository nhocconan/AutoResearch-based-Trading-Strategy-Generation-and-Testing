#!/usr/bin/env python3
"""
6h_12h_1d_BollingerBreakout_TrendVol_v1
Hypothesis: 6-hour breakouts from Bollinger Bands (20,2) with 12-hour trend filter and volume spike confirmation.
Only takes long when price breaks above upper band with volume spike and 12h uptrend, short when breaks below lower band with volume spike and 12h downtrend.
Uses Bollinger Bands instead of Camarilla to avoid saturated family, with focus on 6h timeframe to reduce trade frequency and improve robustness.
"""

name = "6h_12h_1d_BollingerBreakout_TrendVol_v1"
timeframe = "6h"
leverage = 1.0

import numpy as np
import pandas as pd
from mtf_data import get_htf_data, align_htf_to_ltf

def generate_signals(prices):
    n = len(prices)
    if n < 50:
        return np.zeros(n)
    
    high = prices['high'].values
    low = prices['low'].values
    close = prices['close'].values
    volume = prices['volume'].values
    
    # Volume spike: >2.0x 20-period average
    vol_ma = pd.Series(volume).rolling(window=20, min_periods=20).mean().values
    volume_spike = volume > (2.0 * vol_ma)
    
    # Bollinger Bands (20,2)
    ma = pd.Series(close).rolling(window=20, min_periods=20).mean().values
    std = pd.Series(close).rolling(window=20, min_periods=20).std().values
    upper = ma + 2 * std
    lower = ma - 2 * std
    
    # 12h data for trend filter
    df_12h = get_htf_data(prices, '12h')
    if len(df_12h) < 2:
        return np.zeros(n)
    
    # 12h EMA50 for trend filter
    ema_50_12h = pd.Series(df_12h['close']).ewm(span=50, adjust=False, min_periods=50).mean().values
    ema_50_12h_aligned = align_htf_to_ltf(prices, df_12h, ema_50_12h)
    
    signals = np.zeros(n)
    position = 0  # 0: flat, 1: long, -1: short
    
    for i in range(50, n):
        if (np.isnan(ema_50_12h_aligned[i]) or
            np.isnan(upper[i]) or
            np.isnan(lower[i])):
            if position != 0:
                signals[i] = 0.0
                position = 0
            else:
                signals[i] = 0.0
            continue
        
        if position == 0:
            # LONG: Price breaks above upper band + volume spike + price above 12h EMA50
            if (close[i] > upper[i] and 
                volume_spike[i] and 
                close[i] > ema_50_12h_aligned[i]):
                signals[i] = 0.25
                position = 1
            # SHORT: Price breaks below lower band + volume spike + price below 12h EMA50
            elif (close[i] < lower[i] and 
                  volume_spike[i] and 
                  close[i] < ema_50_12h_aligned[i]):
                signals[i] = -0.25
                position = -1
            else:
                signals[i] = 0.0
        elif position == 1:
            # EXIT LONG: Price re-enters Bollinger Bands OR closes below 12h EMA50
            if (close[i] < upper[i] and close[i] > lower[i]) or \
               close[i] < ema_50_12h_aligned[i]:
                signals[i] = 0.0
                position = 0
            else:
                signals[i] = 0.25
        elif position == -1:
            # EXIT SHORT: Price re-enters Bollinger Bands OR closes above 12h EMA50
            if (close[i] < upper[i] and close[i] > lower[i]) or \
               close[i] > ema_50_12h_aligned[i]:
                signals[i] = 0.0
                position = 0
            else:
                signals[i] = -0.25
    
    return signals