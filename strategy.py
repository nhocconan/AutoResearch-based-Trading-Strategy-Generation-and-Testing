#!/usr/bin/env python3
# 12h_1d_Camarilla_R1_S1_Breakout_Volume
# Hypothesis: 12h breakout from daily Camarilla R1/S1 levels with volume confirmation.
# Uses daily Camarilla pivot levels for institutional support/resistance and volume surge to filter false breakouts.
# Designed for low trade frequency (12-37/year) to minimize fee drag in bull and bear markets.
# Works in bull markets via breakouts and bear markets via breakdowns with trend alignment.

name = "12h_1d_Camarilla_R1_S1_Breakout_Volume"
timeframe = "12h"
leverage = 1.0

import numpy as np
import pandas as pd
from mtf_data import get_htf_data, align_htf_to_ltf

def generate_signals(prices):
    n = len(prices)
    if n < 100:
        return np.zeros(n)
    
    # Get daily data for Camarilla pivot calculation
    df_1d = get_htf_data(prices, '1d')
    if len(df_1d) < 30:
        return np.zeros(n)
    
    # 12h OHLCV
    close = prices['close'].values
    high = prices['high'].values
    low = prices['low'].values
    volume = prices['volume'].values
    
    # Calculate daily Camarilla levels (using previous day's OHLC)
    high_1d = df_1d['high'].values
    low_1d = df_1d['low'].values
    close_1d = df_1d['close'].values
    
    # Shift by 1 to use previous day's data (no look-ahead)
    prev_high = np.roll(high_1d, 1)
    prev_low = np.roll(low_1d, 1)
    prev_close = np.roll(close_1d, 1)
    prev_high[0] = high_1d[0]
    prev_low[0] = low_1d[0]
    prev_close[0] = close_1d[0]
    
    # Calculate Camarilla R1 and S1 for previous day
    camarilla_r1 = prev_close + (prev_high - prev_low) * 1.1 / 12
    camarilla_s1 = prev_close - (prev_high - prev_low) * 1.1 / 12
    
    # Align Camarilla levels to 12h timeframe
    camarilla_r1_aligned = align_htf_to_ltf(prices, df_1d, camarilla_r1)
    camarilla_s1_aligned = align_htf_to_ltf(prices, df_1d, camarilla_s1)
    
    # Volume confirmation (2.0x 20-period average)
    vol_ma = pd.Series(volume).rolling(window=20, min_periods=20).mean().values
    
    signals = np.zeros(n)
    position = 0  # 0: flat, 1: long, -1: short
    
    # Warmup
    start_idx = 50
    
    for i in range(start_idx, n):
        # Skip if any critical values are NaN
        if (np.isnan(camarilla_r1_aligned[i]) or
            np.isnan(camarilla_s1_aligned[i]) or
            np.isnan(vol_ma[i])):
            if position != 0:
                signals[i] = 0.0
                position = 0
            continue
        
        # Volume confirmation (2.0x average)
        volume_surge = volume[i] > 2.0 * vol_ma[i]
        
        if position == 0:
            # Long: breakout above Camarilla R1 with volume surge
            if close[i] > camarilla_r1_aligned[i] and volume_surge:
                signals[i] = 0.25
                position = 1
            # Short: breakdown below Camarilla S1 with volume surge
            elif close[i] < camarilla_s1_aligned[i] and volume_surge:
                signals[i] = -0.25
                position = -1
        else:
            # Hold position until opposite signal
            if position == 1:
                signals[i] = 0.25
            else:  # position == -1
                signals[i] = -0.25
    
    return signals