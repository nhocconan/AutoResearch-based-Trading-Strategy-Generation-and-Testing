    # Calculate primary (30m) indicators
    chop = calculate_choppiness(high, low, close, period=14)
    crsi = calculate_connors_rsi(close, rsi_period=3, streak_period=2, rank_period=100)
    atr = calculate_atr(high, low, close, period=14)
    vol_avg = calculate_volume_avg(volume, period=20)
    
    signals = np.zeros(n)
    SIZE = 0.20  # Discrete position size (smaller for 30m)
    
    # Position tracking for stoploss
    in_position = False
    position_side = 0
    entry_price = 0.0
    entry_atr = 0.0
    highest_since_entry = 0.0
    lowest_since_entry = float('inf')
    
    for i in range(150, n):  # Start after warmup for all indicators
        # Skip if indicators not ready
        if np.isnan(atr[i]) or atr[i] <= 1e-10:
            signals[i] = 0.0
            if in_position:
                in_position = False
                position_side = 0
            continue
        if np.isnan(hma_4h_aligned[i]):
            signals[i] = 0.0
            if in_position:
                in_position = False
                position_side = 0
            continue
        if np.isnan(chop[i]) or np.isnan(crsi[i]) or np.isnan(vol_avg[i]):
            signals[i] = 0.0
            if in_position:
                in_position = False
                position_side = 0
            continue
        if vol_avg[i] <= 1e-10:
            signals[i] = 0.0
            if in_position:
                in_position = False
                position_side = 0
            continue
        
        # === SESSION FILTER (8-20 UTC) ===
        utc_hour = get_utc_hour(open_time[i])
        in_session = 8 <= utc_hour <= 20
        
        # === VOLUME FILTER ===
        volume_ok = volume[i] >= 0.5 * vol_avg[i]
        
        # === HTF TREND BIAS (4h HMA) ===
        hma_4h_bull = close[i] > hma_4h_aligned[i]
        hma_4h_bear = close[i] < hma_4h_aligned[i]
        
        # === CHOPPINESS REGIME ===
        trend_regime = chop[i] < 45.0  # Trending market
        range_regime = chop[i] > 55.0  # Range-bound market
        
        # === CONNORS RSI SIGNALS ===
        crsi_oversold = crsi[i] < 30.0  # LOOSE threshold for trades
        crsi_overbought = crsi[i] > 70.0  # LOOSE threshold for trades
        crsi_extreme_oversold = crsi[i] < 15.0  # For range regime
        crsi_extreme_overbought = crsi[i] > 85.0  # For range regime
        
        # === DESIRED SIGNAL (REGIME-ADAPTIVE) ===
        desired_signal = 0.0
        
        # TREND REGIME: Follow HTF trend on CRSI pullback
        if trend_regime and volume_ok and in_session:
            # Long: HTF bullish + CRSI pullback
            if hma_4h_bull and crsi_oversold:
                desired_signal = SIZE
            # Short: HTF bearish + CRSI bounce
            elif hma_4h_bear and crsi_overbought:
                desired_signal = -SIZE
        
        # RANGE REGIME: Mean revert at extremes
        elif range_regime and volume_ok:
            # Long: CRSI extremely oversold
            if crsi_extreme_oversold:
                desired_signal = SIZE
            # Short: CRSI extremely overbought
            elif crsi_extreme_overbought:
                desired_signal = -SIZE
        
        # === STOPLOSS CHECK (Trailing ATR 2.5x) ===
        stoploss_triggered = False
        
        if in_position and position_side > 0:
            highest_since_entry = max(highest_since_entry, close[i])
            stop_price = highest_since_entry - 2.5 * entry_atr
            if close[i] < stop_price:
                stoploss_triggered = True
        
        if in_position and position_side < 0:
            lowest_since_entry = min(lowest_since_entry, close[i])
            stop_price = lowest_since_entry + 2.5 * entry_atr
            if close[i] > stop_price:
                stoploss_triggered = True
        
        if stoploss_triggered:
            desired_signal = 0.0
        
