            # TREND REGIME - RSI pullback + HTF trend
            # Long: RSI 35-55 pullback + HTF bullish
            if 35.0 <= rsi[i] <= 55.0:
                if htf_bull_count >= 1:
                    desired_signal = BASE_SIZE if htf_bull_count >= 2 else REDUCED_SIZE
            
