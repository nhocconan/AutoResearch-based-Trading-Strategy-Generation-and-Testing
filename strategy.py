# Uses 4h trend for direction and 1h breakouts for precise entries. Volume confirms institutional participation.
# Designed for 1h timeframe with target of 60-150 total trades over 4 years (15-37/year).
# Works in bull markets (breakouts with volume) and bear markets (shorts on breakdowns with volume).
# Added session filter (08-20 UTC) to reduce noise trades. Fixed position size 0.20.

