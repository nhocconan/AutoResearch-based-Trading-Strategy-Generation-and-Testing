        rsi_oversold = rsi[i] < 35.0
        rsi_neutral_low = 35.0 <= rsi[i] < 50.0
        rsi_neutral_high = 50.0 <= rsi[i] < 65.0
        rsi_overbought = rsi[i] >= 65.0
        
