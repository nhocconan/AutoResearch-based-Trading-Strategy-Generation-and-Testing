        trend_bull = close[i] > hma_12h_aligned[i]
        trend_bear = close[i] < hma_12h_aligned[i]
        
        # === KAMA TREND CONFIRMATION ===
        kama_bull = close[i] > kama[i]
        kama_bear = close[i] < kama[i]
        
        # === RSI EXTREMES (mean reversion) ===
