        breakout_down = price < lowest_low[i]
        
        # --- Camarilla Pivot Logic ---
        # Breakout continuation: price > R4 (bullish) or price < S4 (bearish)
        breakout_bullish = price > camarilla_r4_aligned[i]
        breakout_bearish = price < camarilla_s4_aligned[i]
        
        # Mean reversion fade: price < R3 (bullish fade) or price > S3 (bearish fade)
        fade_bullish = price < camarilla_r3_aligned[i]
        fade_bearish = price > camarilla_s3_aligned[i]
        
        # --- Exit Logic: ATR-based stoploss ---
        if in_position:
            bars_since_entry += 1
            
            if position_side > 0:  # Long position
                # Stoploss: 2.0*ATR below entry
                stop_level = entry_price - 2.0 * atr[i]
