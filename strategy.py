#!/usr/bin/env python3
"""
6h_RSI_Extreme_Trend_Filter
Hypothesis: Use 6h RSI extremes (RSI < 20 for long, RSI > 80 for short) filtered by weekly trend (price above/below weekly EMA50) and volume confirmation (volume > 1.5x 20-period average). This captures mean reversion in oversold/overbought conditions during strong weekly trends, working in both bull (buy dips in uptrend) and bear (sell rallies in downtrend) markets. Designed for 6h timeframe to limit trades (target 15-35/year) and avoid fee drag.
"""

name = "6h_RSI_Extreme_Trend_Filter"
timeframe = "6h"
leverage = 1.0

import numpy as np
import pandas as pd
from mtf_data import get_htf_data, align_htf_to_ltf

def generate_signals(prices):
    n = len(prices)
    if n < 50:
        return np.zeros(n)
    
    high = prices['high'].values
    low = prices['low'].values
    close = prices['close'].values
    volume = prices['volume'].values
    
    # Calculate RSI (14-period)
    delta = pd.Series(close).diff()
    gain = delta.clip(lower=0)
    loss = -delta.clip(upper=0)
    avg_gain = gain.ewm(alpha=1/14, adjust=False, min_periods=14).mean()
    avg_loss = loss.ewm(alpha=1/14, adjust=False, min_periods=14).mean()
    rs = avg_gain / avg_loss
    rsi = 100 - (100 / (1 + rs))
    rsi = rsi.values
    
    # Get weekly data for trend filter
    df_1w = get_htf_data(prices, '1w')
    if len(df_1w) < 50:
        return np.zeros(n)
    
    close_1w = df_1w['close'].values
    ema_50_1w = pd.Series(close_1w).ewm(span=50, adjust=False, min_periods=50).mean().values
    ema_50_1w_aligned = align_htf_to_ltf(prices, df_1w, ema_50_1w)
    
    # Calculate volume average (20-period) for volume spike filter
    vol_ma_20 = pd.Series(volume).rolling(window=20, min_periods=20).mean().values
    
    signals = np.zeros(n)
    position = 0  # 0: flat, 1: long, -1: short
    
    for i in range(50, n):
        # Skip if any required data is NaN
        if (np.isnan(rsi[i]) or np.isnan(ema_50_1w_aligned[i]) or np.isnan(vol_ma_20[i])):
            signals[i] = 0.0
            continue
        
        # Volume spike condition: current volume > 1.5x 20-period average
        vol_spike = volume[i] > 1.5 * vol_ma_20[i]
        
        if position == 0:
            # LONG: RSI < 20 (oversold) + price above weekly EMA50 + volume spike
            if rsi[i] < 20 and close[i] > ema_50_1w_aligned[i] and vol_spike:
                signals[i] = 0.25
                position = 1
            # SHORT: RSI > 80 (overbought) + price below weekly EMA50 + volume spike
            elif rsi[i] > 80 and close[i] < ema_50_1w_aligned[i] and vol_spike:
                signals[i] = -0.25
                position = -1
            else:
                signals[i] = 0.0
        elif position == 1:
            # EXIT LONG: RSI > 60 (overbought) or price breaks below weekly EMA50
            if rsi[i] > 60 or close[i] < ema_50_1w_aligned[i]:
                signals[i] = 0.0
                position = 0
            else:
                signals[i] = 0.25
        elif position == -1:
            # EXIT SHORT: RSI < 40 (oversold) or price breaks above weekly EMA50
            if rsi[i] < 40 or close[i] > ema_50_1w_aligned[i]:
                signals[i] = 0.0
                position = 0
            else:
                signals[i] = -0.25
    
    return signals