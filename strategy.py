# Hypothesis: 6-hour Elder Ray Index with 1-day volume confirmation and 1-week ADX trend filter
# Elder Ray: Bull Power = High - EMA13, Bear Power = EMA13 - Low
# Long when Bull Power > 0, Bear Power > -0.5 * ATR, volume > 1.5x 20-period average, and weekly ADX > 20
# Short when Bear Power < 0, Bull Power < 0.5 * ATR, volume > 1.5x 20-period average, and weekly ADX > 20
# Exit when opposite power crosses zero
# Stoploss at 2.0 * ATR(14)
# Position size: 0.25 (25% of capital)
# Uses Elder Ray for trend strength, 1-day volume for confirmation, and 1-week ADX for trend filter
# Target: 50-150 total trades over 4 years (12-37/year)

name = "6h_elder_ray_1d_vol_1w_adx_v1"
timeframe = "6h"
leverage = 1.0

def generate_signals(prices):
    n = len(prices)
    if n < 50:
        return np.zeros(n)
    
    # Price data
    close = prices['close'].values
    high = prices['high'].values
    low = prices['low'].values
    volume = prices['volume'].values
    
    # 1-day data for volume confirmation
    df_1d = get_htf_data(prices, '1d')
    if len(df_1d) < 20:
        return np.zeros(n)
    
    # 1-week data for ADX trend filter
    df_1w = get_htf_data(prices, '1w')
    if len(df_1w) < 20:
        return np.zeros(n)
    
    # Calculate 1-day volume average (20-period)
    volume_1d = df_1d['volume'].values
    volume_1d_s = pd.Series(volume_1d)
