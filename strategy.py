            if in_session:
                if rsi_extreme_oversold or (chop_trending and rsi_14[i] < 40):
                    desired_signal = SIZE_STRONG
                else:
                    desired_signal = SIZE_BASE
            else:
                # Outside session: only enter on extreme signals
                if rsi_extreme_oversold and chop_range:
                    desired_signal = SIZE_BASE
        
