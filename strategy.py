import numpy as np
import pandas as pd
from mtf_data import get_htf_data, align_htf_to_ltf

# Hypothesis: 6h strategy combining 1d Supertrend for trend direction and 6h RSI divergence for mean-reversion entries
# Long when 1d Supertrend is bullish and 6h RSI shows bullish divergence (higher low in RSI, lower low in price)
# Short when 1d Supertrend is bearish and 6h RSI shows bearish divergence (lower high in RSI, higher high in price)
# Uses 1d Supertrend for trend filter to avoid counter-trend trades, RSI divergence for precise entries
# Target: 50-150 total trades over 4 years with controlled risk
# ATR-based stoploss to limit drawdown

name = "6h_supertrend1d_rsi_divergence_v1"
timeframe = "6h"
leverage = 1.0

def generate_signals(prices):
    n = len(prices)
    if n < 100:
        return np.zeros(n)
    
    # Price data
    close = prices['close'].values
    high = prices['high'].values
    low = prices['low'].values
    volume = prices['volume'].values
    
    # 1d data for Supertrend
    df_1d = get_htf_data(prices, '1d')
    if len(df_1d) < 10:
        return np.zeros(n)
    
    high_1d = df_1d['high'].values
    low_1d = df_1d['low'].values
    close_1d = df_1d['close'].values
    
    # Supertrend calculation (10, 3.0)
    atr_period = 10
    multiplier = 3.0
    
    # Calculate True Range
    tr1 = high_1d - low_1d
    tr2 = np.abs(high_1d - np.roll(close_1d, 1))
    tr3 = np.abs(low_1d - np.roll(close_1d, 1))
    tr = np.maximum(tr1, np.maximum(tr2, tr3))
    tr[0] = tr1[0]  # First value
    
    # Calculate ATR
    atr = np.zeros_like(close_1d)
    atr[atr_period-1] = np.mean(tr[:atr_period])
    for i in range(atr_period, len(close_1d)):
        atr[i] = (atr[i-1] * (atr_period-1) + tr[i]) / atr_period
    
    # Calculate basic upper and lower bands
    hl2 = (high_1d + low_1d) / 2
    upper_band = hl2 + multiplier * atr
    lower_band = hl2 - multiplier * atr
    
    # Initialize Supertrend
    supertrend = np.zeros_like(close_1d)
    direction = np.ones_like(close_1d)  # 1 for uptrend, -1 for downtrend
    
    supertrend[0] = upper_band[0]
    direction[0] = 1
    
    for i in range(1, len(close_1d)):
        if close_1d[i] > upper_band[i-1]:
            direction[i] = 1
        elif close_1d[i] < lower_band[i-1]:
            direction[i] = -1
        else:
            direction[i] = direction[i-1]
            if direction[i] == 1 and lower_band[i] < lower_band[i-1]:
                lower_band[i] = lower_band[i-1]
            if direction[i] == -1 and upper_band[i] > upper_band[i-1]:
                upper_band[i] = upper_band[i-1]
        
        if direction[i] == 1:
            supertrend[i] = lower_band[i]
        else:
            supertrend[i] = upper_band[i]
    
    # Align Supertrend direction to 6h timeframe
    supertrend_direction_aligned = align_htf_to_ltf(prices, df_1d, direction)
    
    # RSI calculation (14-period) on 6h data
    def calculate_rsi(close_prices, period=14):
        delta = np.diff(close_prices)
        gain = np.where(delta > 0, delta, 0)
        loss = np.where(delta < 0, -delta, 0)
        
        avg_gain = np.zeros_like(close_prices)
        avg_loss = np.zeros_like(close_prices)
        
        avg_gain[period] = np.mean(gain[:period])
        avg_loss[period] = np.mean(loss[:period])
        
        for i in range(period+1, len(close_prices)):
            avg_gain[i] = (avg_gain[i-1] * (period-1) + gain[i-1]) / period
            avg_loss[i] = (avg_loss[i-1] * (period-1) + loss[i-1]) / period
        
        rs = np.where(avg_loss != 0, avg_gain / avg_loss, 0)
        rsi = 100 - (100 / (1 + rs))
        # Handle first period values
        rsi[:period] = 50
        return rsi
    
    rsi = calculate_rsi(close, 14)
    
    signals = np.zeros(n)
    position = 0  # 0: flat, 1: long, -1: short
    entry_price = 0.0
    
    # Track divergence signals
    bullish_divergence = np.zeros(n, dtype=bool)
    bearish_divergence = np.zeros(n, dtype=bool)
    
    # Look for RSI divergence (need at least 3 bars to form)
    for i in range(2, n):
        # Bullish divergence: price makes lower low, RSI makes higher low
        if (low[i] < low[i-1] < low[i-2] or low[i] < low[i-2]) and \
           (rsi[i] > rsi[i-1] > rsi[i-2] or rsi[i] > rsi[i-2]):
            bullish_divergence[i] = True
        # Bearish divergence: price makes higher high, RSI makes lower high
        if (high[i] > high[i-1] > high[i-2] or high[i] > high[i-2]) and \
           (rsi[i] < rsi[i-1] < rsi[i-2] or rsi[i] < rsi[i-2]):
            bearish_divergence[i] = True
    
    for i in range(100, n):
        # Skip if required data not available
        if np.isnan(supertrend_direction_aligned[i]) or np.isnan(rsi[i]):
            if position != 0:
                signals[i] = position * 0.25
            else:
                signals[i] = 0.0
            continue
        
        if position == 1:  # long position
            # Stoploss: 2 * ATR approximation using price range
            if close[i] < entry_price - 2.0 * (high[i] - low[i]):
                signals[i] = 0.0
                position = 0
                entry_price = 0.0
            # Exit: trend turns bearish or RSI shows bearish divergence
            elif supertrend_direction_aligned[i] == -1 or bearish_divergence[i]:
                signals[i] = 0.0
                position = 0
                entry_price = 0.0
            else:
                signals[i] = 0.25
        elif position == -1:  # short position
            # Stoploss: 2 * ATR approximation
            if close[i] > entry_price + 2.0 * (high[i] - low[i]):
                signals[i] = 0.0
                position = 0
                entry_price = 0.0
            # Exit: trend turns bullish or RSI shows bullish divergence
            elif supertrend_direction_aligned[i] == 1 or bullish_divergence[i]:
                signals[i] = 0.0
                position = 0
                entry_price = 0.0
            else:
                signals[i] = -0.25
        else:
            # Look for entries: trend direction + RSI divergence
            # Long: 1d Supertrend bullish + 6h RSI bullish divergence
            if supertrend_direction_aligned[i] == 1 and bullish_divergence[i]:
                signals[i] = 0.25
                position = 1
                entry_price = close[i]
            # Short: 1d Supertrend bearish + 6h RSI bearish divergence
            elif supertrend_direction_aligned[i] == -1 and bearish_divergence[i]:
                signals[i] = -0.25
                position = -1
                entry_price = close[i]
    
    return signals

