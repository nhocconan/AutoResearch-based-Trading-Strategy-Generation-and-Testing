#!/usr/bin/env python3
"""
12h Camarilla Pivot R3/S3 Breakout + 1d EMA34 Trend Filter + Volume Spike
Hypothesis: Camarilla pivot levels act as strong support/resistance; breaks of R3/S3 
indicate institutional participation. 1d EMA34 filters for daily trend alignment. 
Volume spike confirms breakout strength. Works in bull/bear via trend filter. 
Target: 12-25 trades/year on 12h.
"""

import numpy as np
import pandas as pd
from mtf_data import get_htf_data, align_htf_to_ltf

def generate_signals(prices):
    n = len(prices)
    if n < 100:
        return np.zeros(n)
    
    high = prices['high'].values
    low = prices['low'].values
    close = prices['close'].values
    volume = prices['volume'].values
    
    # Load 1d data ONCE before loop for HTF trend filter
    df_1d = get_htf_data(prices, '1d')
    if len(df_1d) < 34:
        return np.zeros(n)
    
    # 1d EMA34 for trend filter
    close_1d = df_1d['close'].values
    ema_34_1d = pd.Series(close_1d).ewm(span=34, adjust=False, min_periods=34).mean().values
    ema_34_1d_aligned = align_htf_to_ltf(prices, df_1d, ema_34_1d)
    
    # Calculate Camarilla pivot levels from previous 1d bar
    # Need to align previous day's H/L/C to current 12h bars
    df_1d_prev = df_1d.copy()
    df_1d_prev['high'] = df_1d_prev['high'].shift(1)  # previous day's high
    df_1d_prev['low'] = df_1d_prev['low'].shift(1)    # previous day's low
    df_1d_prev['close'] = df_1d_prev['close'].shift(1) # previous day's close
    
    # Calculate Camarilla levels for previous day
    H = df_1d_prev['high'].values
    L = df_1d_prev['low'].values
    C = df_1d_prev['close'].values
    
    # Camarilla formulas
    R3 = C + (H - L) * 1.1 / 2
    S3 = C - (H - L) * 1.1 / 2
    
    # Align Camarilla levels to 12h timeframe
    R3_aligned = align_htf_to_ltf(prices, df_1d, R3)
    S3_aligned = align_htf_to_ltf(prices, df_1d, S3)
    
    # Volume confirmation: current volume > 2.0 * 20-period average
    vol_ma = pd.Series(volume).rolling(window=20, min_periods=20).mean().values
    volume_spike = volume > (vol_ma * 2.0)
    
    signals = np.zeros(n)
    position = 0  # 0: flat, 1: long, -1: short
    
    # Start index: need enough for EMA and volume MA
    start_idx = max(34, 20)
    
    for i in range(start_idx, n):
        # Skip if any data not ready
        if (np.isnan(ema_34_1d_aligned[i]) or np.isnan(R3_aligned[i]) or 
            np.isnan(S3_aligned[i]) or np.isnan(vol_ma[i])):
            signals[i] = 0.0
            continue
        
        curr_close = close[i]
        curr_high = high[i]
        curr_low = low[i]
        vol_spike = volume_spike[i]
        
        # Camarilla breakout conditions
        breakout_long = curr_high > R3_aligned[i]   # Break above R3
        breakout_short = curr_low < S3_aligned[i]   # Break below S3
        
        # Trend filter: price above/below 1d EMA34
        uptrend = curr_close > ema_34_1d_aligned[i]
        downtrend = curr_close < ema_34_1d_aligned[i]
        
        if position == 0:
            # Look for entry signals - require: Camarilla breakout + trend alignment + volume
            long_entry = breakout_long and uptrend and vol_spike
            short_entry = breakout_short and downtrend and vol_spike
            
            if long_entry:
                signals[i] = 0.25
                position = 1
            elif short_entry:
                signals[i] = -0.25
                position = -1
            else:
                signals[i] = 0.0
        elif position == 1:
            # Long position management
            # Exit: price retouches S3 level OR trend reverses
            if curr_close < S3_aligned[i] or not uptrend:
                signals[i] = 0.0
                position = 0
            else:
                signals[i] = 0.25
        elif position == -1:
            # Short position management
            # Exit: price retouches R3 level OR trend reverses
            if curr_close > R3_aligned[i] or not downtrend:
                signals[i] = 0.0
                position = 0
            else:
                signals[i] = -0.25
    
    return signals

