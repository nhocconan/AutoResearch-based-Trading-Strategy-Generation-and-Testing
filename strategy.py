        vol_confirmed = vol_ratio > 1.2
        
        # === RSI PULLBACK (LOOSE THRESHOLDS) ===
        rsi_oversold = rsi_7[i] < 55  # Relaxed for more long entries
        rsi_overbought = rsi_7[i] > 45  # Relaxed for more short entries
        
        # === ENTRY LOGIC (SIMPLIFIED FOR MORE TRADES) ===
        desired_signal = 0.0
        
        # LONG entries - require both HTF bullish + trend + volume
        if htf_1d_bull and htf_12h_bull:
            if is_trending and rsi_oversold and vol_confirmed:
                desired_signal = SIZE_STRONG
            elif rsi_oversold and vol_ratio > 1.0:
                # Weaker volume requirement for continuation
                desired_signal = SIZE_BASE
        
