        desired_signal = 0.0
        
        # LONG: vol spike + BB lower touch + RSI oversold + 1d bullish bias
        if vol_spike and bb_touch_lower and rsi_oversold:
            if price_above_1d:
                desired_signal = SIZE_STRONG  # strong conviction with trend
            else:
                desired_signal = SIZE_BASE  # counter-trend mean reversion
        
